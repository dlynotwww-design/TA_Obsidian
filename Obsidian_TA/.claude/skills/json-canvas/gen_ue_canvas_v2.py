import json, random

def gid():
    return ''.join(random.choice('0123456789abcdef') for _ in range(16))

nodes = []
edges = []

def N(x, y, w, h, text, color=None):
    nid = gid()
    node = {'id': nid, 'type': 'text', 'x': x, 'y': y, 'width': w, 'height': h, 'text': text}
    if color: node['color'] = color
    nodes.append(node)
    return nid

def G(x, y, w, h, label, color=None):
    nid = gid()
    node = {'id': nid, 'type': 'group', 'x': x, 'y': y, 'width': w, 'height': h, 'label': label}
    if color: node['color'] = color
    nodes.append(node)
    return nid

def E(frm, to, label=None, fs='bottom', ts='top', color=None):
    eid = gid()
    edge = {'id': eid, 'fromNode': frm, 'toNode': to, 'fromSide': fs, 'toSide': ts}
    if label: edge['label'] = label
    if color: edge['color'] = color
    edges.append(edge)
    return eid

